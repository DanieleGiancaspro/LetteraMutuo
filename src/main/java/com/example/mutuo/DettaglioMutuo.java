package com.example.mutuo;

public class DettaglioMutuo {
    private String nome;
    private String cognome;
    private String indirizzo;
    private String citta;
    private String cap;
    private String dataInizio;
    private String dataFine;
    private String dataAccredito;
    private String dataValuta;
    private String importo;
    private String numeroMutuo;

    // Getter e Setter
    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }

    public String getCognome() { return cognome; }
    public void setCognome(String cognome) { this.cognome = cognome; }

    public String getIndirizzo() { return indirizzo; }
    public void setIndirizzo(String indirizzo) { this.indirizzo = indirizzo; }

    public String getCitta() { return citta; }
    public void setCitta(String citta) { this.citta = citta; }

    public String getCap() { return cap; }
    public void setCap(String cap) { this.cap = cap; }

    public String getDataInizio() { return dataInizio; }
    public void setDataInizio(String dataInizio) { this.dataInizio = dataInizio; }

    public String getDataFine() { return dataFine; }
    public void setDataFine(String dataFine) { this.dataFine = dataFine; }

    public String getDataAccredito() { return dataAccredito; }
    public void setDataAccredito(String dataAccredito) { this.dataAccredito = dataAccredito; }

    public String getDataValuta() { return dataValuta; }
    public void setDataValuta(String dataValuta) { this.dataValuta = dataValuta; }

    public String getImporto() { return importo; }
    public void setImporto(String importo) { this.importo = importo; }

    public String getNumeroMutuo() { return numeroMutuo; }
    public void setNumeroMutuo(String numeroMutuo) { this.numeroMutuo = numeroMutuo; }
}
